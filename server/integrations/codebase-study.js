  function isMeaningfulExportName(name) {
    const normalized = String(name || '').trim();
    if (!normalized) {
      return false;
    }
    const lowered = normalized.toLowerCase();
    if (LOW_SIGNAL_EXPORT_NAMES.has(lowered)) {
      return false;
    }
    if (normalized.length <= 1) {
      return false;
    }
    if (/^test\d*$/i.test(normalized)) {
      return false;
    }
    return true;
  }

  function isLowSignalHotspotFile(repoPath) {
    return LOW_SIGNAL_HOTSPOT_BASENAMES.has(path.basename(toRepoPath(repoPath)));
  }

  function isStructuredContentFile(repoPath) {
    const normalized = toRepoPath(repoPath).toLowerCase();
    if (!normalized) {
      return false;
    }
    if (/\.(json|ya?ml|toml|ini)$/.test(normalized)) {
      return true;
    }
    return /(^|\/)(lang|locales?|i18n|manifests?|schemas?|fixtures|contracts)\//.test(normalized);
  }

  function isExecutableSurfaceFile(repoPath) {
    const normalized = toRepoPath(repoPath);
    const ext = path.extname(normalized).toLowerCase();
    if (SYMBOL_INDEX_EXTENSIONS.has(ext)) {
      return true;
    }
    const base = path.basename(normalized).toLowerCase();
    return base === 'index.html'
      || base === 'gulpfile.js'
      || base === 'dev-server.js'
      || base === 'app.xaml'
      || base === 'app.xaml.cs';
  }

  function isPrimaryRuntimeSurface(repoPath) {
    const normalized = toRepoPath(repoPath).toLowerCase();
    if (!normalized || normalized.startsWith('tools/')) {
      return false;
    }
    return normalized.startsWith('src/')
      || normalized.startsWith('server/')
      || normalized.startsWith('app/')
      || normalized.startsWith('client/')
      || normalized.startsWith('dashboard/')
      || normalized.startsWith('bin/')
      || normalized.startsWith('cli/');
  }

  function getHotspotStructuralBoost(repoPath) {
    const normalized = toRepoPath(repoPath).toLowerCase();
    if (!normalized) {
      return 0;
    }

    let boost = 0;
    if (isPrimaryRuntimeSurface(normalized)) {
      boost += 4;
    }
    if (/(?:^|\/)(?:application|domain|core|runtime|services?|infrastructure|ledger|accounting|banking|workflow|pipeline|state|parser|execution|setup)\//.test(normalized)) {
      boost += 6;
    }
    if (/(?:engine|service|manager|coordinator|provider|handler|dispatcher|repository|store|validator|processor|orchestrator)\.[^.]+$/.test(normalized)) {
      boost += 8;
    }
    if (/(?:initializer|seeder|bootstrapper|builder)\.[^.]+$/.test(normalized)) {
      boost += 6;
    }
    if (/(?:controller)\.[^.]+$/.test(normalized)) {
      boost += 5;
    }
    if (/(?:viewmodel|window|dialog|page|screen|panel|form|view)\.[^.]+$/.test(normalized)) {
      boost += 2;
    }
    if (/(?:tabviewmodel|window|dialog|page|screen)\.[^.]+$/.test(normalized)) {
      boost -= 3;
    }
    return boost;
  }

  function isShallowToolEntrypoint(filePath, inboundDependents, outboundDependencies, exportCount) {
    const normalized = toRepoPath(filePath).toLowerCase();
    if (!normalized || !normalized.startsWith('tools/') || !isLikelyEntrypoint(normalized)) {
      return false;
    }
    return (Number(inboundDependents) || 0) <= 1
      && (Number(outboundDependencies) || 0) <= 3
      && (Number(exportCount) || 0) <= 2;
  }

  function getHotspotRoleBucket(repoPath) {
    const normalized = toRepoPath(repoPath).toLowerCase();
    if (!normalized) {
      return 'module';
    }
    if (isLikelyEntrypoint(normalized)) {
      return 'entrypoint';
    }
    if (/(?:engine|service|manager|coordinator|provider|handler|dispatcher|validator|processor|orchestrator)\.[^.]+$/.test(normalized)) {
      return 'logic';
    }
    if (/(?:initializer|seeder|bootstrapper|builder|migration)\.[^.]+$/.test(normalized) || /(^|\/)setup\//.test(normalized)) {
      return 'setup';
    }
    if (/(?:repository|store|client|gateway|adapter|dbcontext)\.[^.]+$/.test(normalized)) {
      return 'integration';
    }
    if (/(?:viewmodel|window|dialog|page|screen|panel|form|view|promptservice)\.[^.]+$/.test(normalized)) {
      return 'presentation';
    }
    if (isStructuredContentFile(normalized)) {
      return 'content';
    }
    return 'module';
  }

  function getHotspotAreaKey(filePath) {
    const segments = toRepoPath(filePath).split('/').filter(Boolean);
    if (segments.length === 0) {
      return '';
    }
    if (segments[0] === 'src' && segments.length >= 3) {
      return segments.slice(0, 3).join('/');
    }
    return segments.slice(0, Math.min(2, segments.length)).join('/');
  }

  function getHotspotCategoryPriority(roleBucket, signalType) {
    if (roleBucket === 'entrypoint') return 6;
    if (roleBucket === 'logic') return 5;
    if (roleBucket === 'setup') return 4;
    if (roleBucket === 'integration') return 3;
    if (signalType === 'runtime') return 2;
    if (roleBucket === 'module') return 1;
    if (roleBucket === 'presentation') return 0;
    if (roleBucket === 'content') return -1;
    return 0;
  }

  function selectDiverseHotspots(candidates, limit = HOTSPOT_LIMIT) {
    const selected = [];
    const selectedFiles = new Set();
    const areaCounts = new Map();
    const roleCounts = new Map();

    const trySelect = (candidate, enforceDiversity) => {
      const areaKey = candidate.area_key || getHotspotAreaKey(candidate.file);
      const roleBucket = candidate.role_bucket || getHotspotRoleBucket(candidate.file);
      const areaLimit = roleBucket === 'presentation' ? 1 : 2;
      const roleLimit = roleBucket === 'presentation' ? 2 : 3;
      if (selectedFiles.has(candidate.file)) {
        return false;
      }
      if (enforceDiversity) {
        if ((areaCounts.get(areaKey) || 0) >= areaLimit) {
          return false;
        }
        if ((roleCounts.get(roleBucket) || 0) >= roleLimit) {
          return false;
        }
      }

      selected.push(candidate);
      selectedFiles.add(candidate.file);
      areaCounts.set(areaKey, (areaCounts.get(areaKey) || 0) + 1);
      roleCounts.set(roleBucket, (roleCounts.get(roleBucket) || 0) + 1);
      return true;
    };

    for (const candidate of candidates) {
      if (selected.length >= limit) {
        break;
      }
      trySelect(candidate, true);
    }

    for (const candidate of candidates) {
      if (selected.length >= limit) {
        break;
      }
      trySelect(candidate, false);
    }

    return selected;
  }

  function getEntrypointCorePathBoost(filePath, moduleMap, hotspotFiles, reverseDeps) {
    const normalized = toRepoPath(filePath);
    const moduleEntry = moduleMap.get(normalized);
    if (!moduleEntry) {
      return { boost: 0, evidence: [] };
    }

    const evidence = [];
    const directDeps = uniquePaths(moduleEntry.deps || []);
    const directHotspots = directDeps.filter((dependency) => hotspotFiles.has(dependency));
    const coreDeps = directDeps.filter((dependency) => ['logic', 'setup', 'integration'].includes(getHotspotRoleBucket(dependency)));
    const secondHopHotspots = uniquePaths(directDeps.flatMap((dependency) => moduleMap.get(dependency)?.deps || []))
      .filter((dependency) => hotspotFiles.has(dependency));

    let boost = 0;
    if (directHotspots.length > 0) {
      boost += Math.min(14, directHotspots.length * 7);
      evidence.push('Direct dependencies reach hotspot-ranked implementation seams.');
    }
    if (coreDeps.length > 0) {
      boost += Math.min(10, coreDeps.length * 4);
      evidence.push('Direct dependencies lead into core logic or setup layers.');
    }
    if (secondHopHotspots.length > 0) {
      boost += Math.min(6, secondHopHotspots.length * 2);
      evidence.push('One dependency hop reaches a high-signal implementation seam.');
    }
    if (directDeps.length > 0) {
      boost += Math.min(4, directDeps.length);
    }
    const inboundDependents = reverseDeps.get(normalized)?.size || 0;
    if (inboundDependents > 0) {
      boost += Math.min(4, inboundDependents);
      evidence.push('Referenced by additional modules, suggesting it is a shared runtime seam.');
    }

    return {
      boost,
      evidence,
    };
  }

  function scoreToConfidence(score) {
    if (score >= 24) {
      return 'high';
    }
    if (score >= 12) {
      return 'medium';
    }
    return 'low';
  }

  function buildDetectionSummary(repoSignals) {
    if (!repoSignals || typeof repoSignals !== 'object') {
      return null;
    }
    return {
      archetype: repoSignals.archetype || 'generic-javascript-repo',
      confidence: repoSignals.confidence || 'medium',
      frameworks: uniqueStrings(repoSignals.frameworks || []),
      traits: uniqueStrings(repoSignals.traits || []),
      evidence: uniqueStrings(repoSignals.evidence || []),
    };
  }

  function toFrequencyEntries(values, limit = 5) {
    return Array.from(countOccurrences(values).entries())
      .sort((left, right) => right[1] - left[1] || left[0].localeCompare(right[0]))
      .slice(0, limit)
      .map(([name, count]) => ({ name, count }));
  }

  function formatFrequencyList(items, maxItems = 3) {
    return formatInlineList((items || []).slice(0, maxItems).map(item => `\`${item.name}\` (${item.count})`));
  }

  function buildReverseDependencyMap(entries) {
    const moduleFiles = new Set((entries || []).map(entry => entry.file));
    const reverseDeps = new Map();
    for (const entry of entries || []) {
      for (const dep of entry.deps || []) {
        if (!moduleFiles.has(dep)) {
          continue;
        }
        if (!reverseDeps.has(dep)) {
          reverseDeps.set(dep, new Set());
        }
        reverseDeps.get(dep).add(entry.file);
      }
    }
    return reverseDeps;
  }

  function buildSubsystemRows(entries, context = {}) {
    const trackedFiles = uniquePaths(context.trackedFiles);
    const pendingFiles = uniquePaths(context.pendingFiles);
    const reverseDeps = context.reverseDeps || new Map();
    const activeProfile = context.activeProfile || null;
    const subsystemLookup = context.subsystemLookup || buildSubsystemLookup([...trackedFiles, ...entries.map(entry => entry.file)], activeProfile);
    const trackedBySubsystem = new Map();
    const pendingBySubsystem = new Map();
    const entriesBySubsystem = new Map();
    const subsystemMeta = new Map();

    const recordSubsystem = (filePath) => {
      const subsystem = subsystemLookup.get(filePath) || getSubsystemForFile(filePath, activeProfile);
      subsystemLookup.set(filePath, subsystem);
      subsystemMeta.set(subsystem.id, subsystem);
      return subsystem;
    };

    for (const filePath of trackedFiles) {
      const subsystem = recordSubsystem(filePath);
      if (!trackedBySubsystem.has(subsystem.id)) {
        trackedBySubsystem.set(subsystem.id, []);
      }
      trackedBySubsystem.get(subsystem.id).push(filePath);
    }

    for (const filePath of pendingFiles) {
      const subsystem = recordSubsystem(filePath);
      if (!pendingBySubsystem.has(subsystem.id)) {
        pendingBySubsystem.set(subsystem.id, []);
      }
      pendingBySubsystem.get(subsystem.id).push(filePath);
    }

    for (const entry of entries || []) {
      const subsystem = recordSubsystem(entry.file);
      if (!entriesBySubsystem.has(subsystem.id)) {
        entriesBySubsystem.set(subsystem.id, []);
      }
      entriesBySubsystem.get(subsystem.id).push(entry);
    }

    const subsystemIds = uniqueStrings([
      ...subsystemMeta.keys(),
      ...trackedBySubsystem.keys(),
      ...pendingBySubsystem.keys(),
      ...entriesBySubsystem.keys(),
    ]);

    return subsystemIds
      .map((subsystemId) => {
        const subsystem = subsystemMeta.get(subsystemId);
        const subsystemEntries = (entriesBySubsystem.get(subsystemId) || []).slice();
        const tracked = uniquePaths(trackedBySubsystem.get(subsystemId) || []);
        const pending = uniquePaths(pendingBySubsystem.get(subsystemId) || []);
        const rankedEntries = subsystemEntries
          .map((entry) => {
            const inboundDependents = reverseDeps.get(entry.file)?.size || 0;
            return {
              entry,
              inboundDependents,
              score: (inboundDependents * 4) + ((entry.deps || []).length * 2) + (entry.exports || []).length + (isLikelyEntrypoint(entry.file) ? 3 : 0),
            };
          })
          .sort((left, right) => right.score - left.score || left.entry.file.localeCompare(right.entry.file));

        const representativeFiles = uniquePaths([
          ...rankedEntries.map(item => item.entry.file),
          ...tracked,
        ]).slice(0, 5);
        const entrypoints = representativeFiles.filter(isLikelyEntrypoint).slice(0, 4);
        const keyExports = subsystem.id === 'validation-tests'
          ? []
          : toFrequencyEntries(
            subsystemEntries.flatMap(entry => entry.exports || []).filter(isMeaningfulExportName),
            8
          );
        const keyDependencies = toFrequencyEntries(subsystemEntries.flatMap(entry => entry.deps || []), 8);
        const centralFiles = rankedEntries.slice(0, 3).map((item) => ({
          file: item.entry.file,
          inbound_dependents: item.inboundDependents,
          outbound_dependencies: (item.entry.deps || []).length,
          export_count: (item.entry.exports || []).length,
        }));

        let overview = subsystem?.description || 'Repository subsystem.';
        overview += ` Coverage: ${subsystemEntries.length}/${tracked.length || subsystemEntries.length} indexed`;
        if (pending.length > 0) {
          overview += `, ${pending.length} pending`;
        }
        overview += '.';
        if (representativeFiles.length > 0) {
          overview += ` Representative files: ${formatCodeList(representativeFiles)}.`;
        }
        if (subsystem.id === 'validation-tests') {
          overview += ' Test-heavy area; representative suites and helper dependencies matter more than exported symbols.';
        } else if (keyExports.length > 0) {
          overview += ` Key exports: ${formatFrequencyList(keyExports)}.`;
        }
        if (keyDependencies.length > 0) {
          overview += ` Key dependencies: ${formatFrequencyList(keyDependencies, 2)}.`;
        }

        return {
          id: subsystem.id,
          label: subsystem.label,
          description: subsystem.description,
          summary_priority: getSubsystemPriority(activeProfile, subsystem.id),
          overview,
          coverage: {
            tracked_files: tracked.length,
            indexed_modules: subsystemEntries.length,
            pending_files: pending.length,
          },
          representative_files: representativeFiles,
          entrypoints: entrypoints.length > 0 ? entrypoints : representativeFiles.slice(0, 3),
          key_exports: keyExports,
          key_dependencies: keyDependencies,
          central_files: centralFiles,
        };
      })
      .sort((left, right) => {
        const priorityDiff = (right.summary_priority || 0) - (left.summary_priority || 0);
        if (priorityDiff !== 0) {
          return priorityDiff;
        }
        const moduleDiff = right.coverage.indexed_modules - left.coverage.indexed_modules;
        if (moduleDiff !== 0) {
          return moduleDiff;
        }
        const trackedDiff = right.coverage.tracked_files - left.coverage.tracked_files;
        if (trackedDiff !== 0) {
          return trackedDiff;
        }
        return left.label.localeCompare(right.label);
      });
  }

  function isLikelyEntrypoint(repoPath) {
    const normalized = toRepoPath(repoPath);
    const base = path.basename(normalized);
    const lowerBase = base.toLowerCase();
    return normalized === 'server/index.js'
      || normalized === 'server/task-manager.js'
      || normalized === 'server/tools.js'
      || normalized === 'dashboard/src/main.jsx'
      || normalized === 'dashboard/src/App.jsx'
      || normalized === 'bin/torque.js'
      || normalized === 'cli/torque-cli.js'
      || lowerBase === 'program.cs'
      || lowerBase === 'app.xaml.cs'
      || lowerBase === '__main__.py'
      || lowerBase === 'cli.py'
      || lowerBase === 'server.py'
      || lowerBase === 'main.py'
      || lowerBase === 'main.cs'
      || lowerBase === 'index.js'
      || lowerBase === 'main.jsx'
      || lowerBase === 'main.js'
      || lowerBase === 'app.js'
      || lowerBase === 'bootstrap.js'
      || lowerBase === 'bootstrap.ts';
  }

  function inferGenericEntrypointCandidate(filePath, repoMetadata, repoSignals = null) {
    const normalized = toRepoPath(filePath);
    if (!normalized || isStructuredContentFile(normalized) || isTestFile(normalized)) {
      return null;
    }

    const base = path.basename(normalized).toLowerCase();
    const segments = normalized.split('/').filter(Boolean);
    let score = 0;
    let role = '';
    const evidence = [];

    if (normalized === toRepoPath(repoMetadata?.package_main)) {
      score = 180;
      role = 'Primary package runtime entrypoint';
      evidence.push('Detected from package.json main.');
    } else if ((repoMetadata?.bin_files || []).includes(normalized)) {
      score = 175;
      role = 'Primary CLI entrypoint';
      evidence.push('Detected from package.json bin.');
    } else if (normalized === 'server/index.js') {
      score = 170;
      role = 'Server bootstrap and background scheduler startup';
      evidence.push('Recognized server bootstrap path.');
    } else if (normalized === 'server/task-manager.js') {
      score = 166;
      role = 'Top-level task execution coordinator';
      evidence.push('Recognized task orchestration seam.');
    } else if (normalized === 'server/tools.js') {
      score = 164;
      role = 'Tool catalog and dispatch surface';
      evidence.push('Recognized tool dispatch seam.');
    } else if (normalized === 'server/api/v2-dispatch.js') {
      score = 162;
      role = 'Primary control-plane dispatch surface';
      evidence.push('Recognized control-plane dispatch seam.');
    } else if (normalized === 'dashboard/src/main.jsx') {
      score = 160;
      role = 'Dashboard client bootstrap';
      evidence.push('Recognized UI bootstrap path.');
    } else if (normalized === 'dashboard/src/App.jsx') {
      score = 158;
      role = 'Dashboard shell composition';
      evidence.push('Recognized UI shell path.');
    } else if (normalized === 'bin/torque.js') {
      score = 156;
      role = 'CLI bootstrap for local command use';
      evidence.push('Recognized CLI bootstrap path.');
    } else if (base === 'program.cs') {
      score = 152;
      role = 'Primary .NET application or CLI entrypoint';
      evidence.push('Matched conventional .NET Program.cs startup file.');
    } else if (base === 'app.xaml.cs') {
      score = 149;
      role = 'Desktop application startup entrypoint';
      evidence.push('Matched WPF App.xaml.cs startup file.');
    } else if (base === '__main__.py') {
      score = 146;
      role = 'Primary Python module entrypoint';
      evidence.push('Matched Python __main__ entrypoint.');
    } else if (base === 'cli.py') {
      score = 142;
      role = 'Python CLI bootstrap';
      evidence.push('Matched Python CLI entrypoint.');
    } else if (base === 'server.py') {
      score = 140;
      role = 'Python service or server bootstrap';
      evidence.push('Matched Python server entrypoint.');
    } else if (base === 'main.py') {
      score = 136;
      role = 'Primary Python runtime entrypoint';
      evidence.push('Matched Python main entrypoint.');
    } else if (base === 'index.js' || base === 'index.ts' || base === 'main.js' || base === 'main.ts') {
      score = 132;
      role = 'Primary JavaScript/TypeScript runtime entrypoint';
      evidence.push('Matched common runtime entrypoint name.');
    } else if (base === 'app.js' || base === 'app.ts' || base === 'app.jsx' || base === 'app.tsx') {
      score = 128;
      role = 'Application bootstrap entrypoint';
      evidence.push('Matched common application bootstrap file.');
    } else if (base === 'bootstrap.js' || base === 'bootstrap.ts' || base === 'bootstrap.py') {
      score = 126;
      role = 'Runtime bootstrap entrypoint';
      evidence.push('Matched bootstrap entrypoint name.');
    }

    if (!score && isLikelyEntrypoint(normalized)) {
      score = 120;
      role = 'Likely runtime or UI entrypoint';
      evidence.push('Matched generic entrypoint heuristic.');
    }
    if (!score) {
      return null;
    }

    if (normalized.startsWith('src/')) {
      score += 8;
      evidence.push('Located under src/, suggesting a primary implementation surface.');
    }
    if (normalized.startsWith('tools/')) {
      score += /(?:^|\/)(?:server|cli|launcher|runner|peek[_-]server)\b/i.test(normalized) ? 12 : -4;
    }
    if (normalized.startsWith('bin/') || normalized.startsWith('cli/')) {
      score += 10;
    }
    if ((repoSignals?.frameworks || []).includes('.NET') && (base === 'program.cs' || base === 'app.xaml.cs')) {
      score += 10;
    }
    if ((repoSignals?.frameworks || []).includes('Python') && (base === '__main__.py' || base === 'cli.py' || base === 'server.py' || base === 'main.py')) {
      score += 10;
    }
    if ((repoSignals?.archetype || '').includes('polyglot')) {
      score += 6;
    }

    score -= Math.max(0, segments.length - 3) * 4;

    return {
      file: normalized,
      role,
      confidence: score >= 155 ? 'high' : (score >= 128 ? 'medium' : 'low'),
      evidence: uniqueStrings(evidence),
      score,
    };
  }

  function buildSubsystemRelationships(entries, subsystemLookup, activeProfile) {
    const moduleFiles = new Set((entries || []).map(entry => entry.file));
    const edgeMap = new Map();

    for (const entry of entries || []) {
      const sourceSubsystem = subsystemLookup.get(entry.file) || getSubsystemForFile(entry.file, activeProfile);
      for (const dep of entry.deps || []) {
        if (!moduleFiles.has(dep)) {
          continue;
        }
        const targetSubsystem = subsystemLookup.get(dep) || getSubsystemForFile(dep, activeProfile);
        if (sourceSubsystem.id === targetSubsystem.id) {
          continue;
        }
        const key = `${sourceSubsystem.id}->${targetSubsystem.id}`;
        if (!edgeMap.has(key)) {
          edgeMap.set(key, {
            from: sourceSubsystem.id,
            from_label: sourceSubsystem.label,
            to: targetSubsystem.id,
            to_label: targetSubsystem.label,
            count: 0,
            example_edges: [],
            evidence_files: [],
          });
        }
        const edge = edgeMap.get(key);
        edge.count += 1;
        const example = `${entry.file} -> ${dep}`;
        if (edge.example_edges.length < 3 && !edge.example_edges.includes(example)) {
          edge.example_edges.push(example);
        }
        for (const evidenceFile of [entry.file, dep]) {
          if (edge.evidence_files.length >= 6 || edge.evidence_files.includes(evidenceFile)) {
            continue;
          }
          edge.evidence_files.push(evidenceFile);
        }
      }
    }

    return Array.from(edgeMap.values())
      .sort((left, right) => right.count - left.count || `${left.from_label}:${left.to_label}`.localeCompare(`${right.from_label}:${right.to_label}`))
      .slice(0, RELATIONSHIP_LIMIT);
  }

  function buildHotspotReason(entry, inboundDependents, subsystemLabel) {
    const outboundDependencies = (entry.deps || []).length;
    const exportCount = (entry.exports || []).length;
    if (isStructuredContentFile(entry.file)) {
      if (inboundDependents >= 2) {
        return `Structured content or contract file reused by ${inboundDependents} indexed modules in ${subsystemLabel}.`;
      }
      return `Structured content or contract file that anchors ${subsystemLabel}.`;
    }
    if (inboundDependents >= 3 && outboundDependencies >= 3) {
      return `High fan-in (${inboundDependents}) and fan-out (${outboundDependencies}) inside ${subsystemLabel}.`;
    }
    if (inboundDependents >= 3) {
      return `Shared internal dependency reused by ${inboundDependents} indexed modules in ${subsystemLabel}.`;
    }
    if (outboundDependencies >= 5) {
      return `Touches many dependencies (${outboundDependencies}), so changes here can ripple broadly.`;
    }
    if (exportCount >= 4) {
      return `Exports a broad surface (${exportCount} symbols), making it a likely API seam.`;
    }
    return `Representative file in ${subsystemLabel}.`;
  }

  function buildHotspots(entries, reverseDeps, subsystemLookup, activeProfile, repoSignals = null) {
    const hasPrimaryAppSurface = (entries || []).some((entry) => {
      const normalized = toRepoPath(entry?.file).toLowerCase();
      if (!isPrimaryRuntimeSurface(normalized)) {
        return false;
      }
      return isLikelyEntrypoint(normalized)
        || (entry?.deps || []).length > 0
        || (entry?.exports || []).length > 0
        || getHotspotStructuralBoost(normalized) >= 10;
    });
    const candidates = (entries || [])
      .map((entry) => {
        const inboundDependents = reverseDeps.get(entry.file)?.size || 0;
        const subsystem = subsystemLookup.get(entry.file) || getSubsystemForFile(entry.file, activeProfile);
        const outboundDependencies = (entry.deps || []).length;
        const exportCount = (entry.exports || []).length;
        const lowSignalPenalty = isLowSignalHotspotFile(entry.file) ? 25 : 0;
        const structuredContent = isStructuredContentFile(entry.file);
        const executableSurface = isExecutableSurfaceFile(entry.file);
        const contentPenalty = structuredContent
          ? ((repoSignals?.archetype === 'content-heavy-javascript-repo' || (repoSignals?.traits || []).includes('content')) ? 8 : 18)
          : 0;
        const executableBoost = executableSurface ? 6 : 0;
        const structuralBoost = getHotspotStructuralBoost(entry.file);
        const shallowToolPenalty = hasPrimaryAppSurface && isShallowToolEntrypoint(
          entry.file,
          inboundDependents,
          outboundDependencies,
          exportCount
        ) ? 18 : 0;
        const score = ((inboundDependents * 4) + (outboundDependencies * 2) + exportCount + (isLikelyEntrypoint(entry.file) ? 3 : 0))
          - lowSignalPenalty
          - contentPenalty
          - shallowToolPenalty
          + executableBoost
          + structuralBoost;
        const roleBucket = getHotspotRoleBucket(entry.file);
        return {
          file: entry.file,
          subsystem: subsystem.id,
          subsystem_label: subsystem.label,
          inbound_dependents: inboundDependents,
          outbound_dependencies: outboundDependencies,
          export_count: exportCount,
          signal_type: structuredContent ? 'content' : (executableSurface ? 'runtime' : 'module'),
          reason: buildHotspotReason(entry, inboundDependents, subsystem.label),
          confidence: scoreToConfidence(score),
          evidence: {
            inbound_dependents: inboundDependents,
            outbound_dependencies: outboundDependencies,
            export_count: exportCount,
            structured_content: structuredContent,
            executable_surface: executableSurface,
          },
          role_bucket: roleBucket,
          area_key: getHotspotAreaKey(entry.file),
          category_priority: getHotspotCategoryPriority(roleBucket, structuredContent ? 'content' : (executableSurface ? 'runtime' : 'module')),
          score,
        };
      })
      .sort((left, right) => (
        right.score - left.score
        || right.category_priority - left.category_priority
        || left.file.localeCompare(right.file)
      ));

    return selectDiverseHotspots(candidates, HOTSPOT_LIMIT)
      .map(({ score, area_key, category_priority, role_bucket, ...rest }) => rest);
  }

  function collectInternalDependencies(moduleMap, sourceFiles, availableFiles, limit = 5) {
    return uniquePaths((sourceFiles || [])
      .flatMap((filePath) => moduleMap.get(filePath)?.deps || [])
      .filter((filePath) => availableFiles.has(filePath) && !isTestFile(filePath)))
      .slice(0, limit);
  }

  function isConfigOrContractFile(repoPath) {
    const normalized = toRepoPath(repoPath).toLowerCase();
    if (!normalized) {
      return false;
    }
    if (normalized === 'package.json' || normalized === 'package-lock.json' || normalized === 'tsconfig.json' || normalized === 'jsconfig.json') {
      return true;
    }
    if (/\.(json|ya?ml|toml|ini)$/.test(normalized)) {
      return true;
    }
    return /(^|\/)(config|configs|manifests?|lang|locales?|i18n|schemas?|fixtures)\//.test(normalized);
  }

  function buildGenericFlowDefinitions({ repoMetadata, trackedFiles, modules, entrypoints, hotspots } = {}) {
    const availableFiles = new Set(uniquePaths(trackedFiles));
    const moduleMap = buildModuleEntryMap(modules);
    const hotspotFiles = uniquePaths((hotspots || []).map(item => item.file));
    const entrypointFiles = uniquePaths((entrypoints || []).map(item => item.file));
    const runtimeEntrypoints = uniquePaths([
      toRepoPath(repoMetadata?.package_main),
      ...(repoMetadata?.bin_files || []),
      ...entrypointFiles,
      ...hotspotFiles,
    ]).filter(filePath => availableFiles.has(filePath)).slice(0, 4);
    const runtimeImplementation = collectInternalDependencies(moduleMap, runtimeEntrypoints, availableFiles, 5);

    const configFiles = uniquePaths((trackedFiles || []).filter(isConfigOrContractFile)).slice(0, 5);
    const configConsumers = uniquePaths((modules || [])
      .filter(entry => (entry.deps || []).some(dep => configFiles.includes(dep)))
      .map(entry => entry.file))
      .slice(0, 4);

    const testFiles = uniquePaths((trackedFiles || []).filter(isTestFile)).slice(0, 5);
    const validatedFiles = testFiles.length > 0
      ? uniquePaths((testFiles || [])
        .flatMap((filePath) => moduleMap.get(filePath)?.deps || [])
        .filter(filePath => availableFiles.has(filePath) && !isTestFile(filePath)))
        .slice(0, 5)
      : uniquePaths([...runtimeEntrypoints, ...runtimeImplementation, ...hotspotFiles]).slice(0, 5);
    const validationSurfaces = testFiles.length > 0
      ? testFiles
      : uniquePaths([
        'package.json',
        'package-lock.json',
        ...(repoMetadata?.bin_files || []),
      ]).filter(filePath => availableFiles.has(filePath)).slice(0, 4);

    const definitions = [];
    if (runtimeEntrypoints.length > 0 || runtimeImplementation.length > 0) {
      definitions.push({
        id: GENERIC_FLOW_IDS.ENTRY_RUNTIME,
        label: 'Entrypoints and runtime flow',
        summary: 'How a maintainer should enter the repo, then follow the first-hop runtime or UI implementation path.',
        questions: [
          'Where should a new reader start, and which files carry the main runtime surface deeper into the repo?',
        ],
        evidence_quality: 'heuristic',
        evidence: ['Synthesized from entrypoints, hotspots, and internal dependency hops.'],
        steps: [
          {
            label: 'Entrypoints',
            description: 'Top-level files that expose the main CLI, UI, or runtime surface.',
            files: runtimeEntrypoints,
          },
          {
            label: 'Runtime implementation',
            description: 'First-hop implementation files that the entrypoints immediately delegate into.',
            files: runtimeImplementation,
          },
        ],
      });
    }

    if (configFiles.length > 0 || configConsumers.length > 0) {
      definitions.push({
        id: GENERIC_FLOW_IDS.CONFIG_CONTRACTS,
        label: 'Configuration and contract flow',
        summary: 'How config, manifests, locale packs, schemas, or other structured assets connect back to consuming code.',
        questions: [
          'Which files define the repo’s data contracts or config surfaces, and which modules consume them?',
        ],
        evidence_quality: 'heuristic',
        evidence: ['Synthesized from config/content files and the modules that import them.'],
        steps: [
          {
            label: 'Source-of-truth files',
            description: 'Config, schema, manifest, or content files that act like contracts.',
            files: configFiles,
          },
          {
            label: 'Consumers',
            description: 'Runtime or tooling modules that read those contracts.',
            files: configConsumers,
          },
        ],
      });
    }

    if (validationSurfaces.length > 0 || validatedFiles.length > 0) {
      definitions.push({
        id: GENERIC_FLOW_IDS.CHANGE_VALIDATION,
        label: 'Change and validation flow',
        summary: 'Which tests, harnesses, or build surfaces should be checked after editing the repo’s riskier seams.',
        questions: [
          'If you change a key module, which validation surfaces and neighboring files should you inspect first?',
        ],
        evidence_quality: 'heuristic',
        evidence: ['Synthesized from tests, scripts, build surfaces, and protected implementation files.'],
        steps: [
          {
            label: testFiles.length > 0 ? 'Tests and harnesses' : 'Validation surfaces',
            description: testFiles.length > 0
              ? 'Tests and harnesses that appear to cover the nearby implementation seams.'
              : 'Build or package surfaces that are still useful validation starting points when tests are sparse.',
            files: validationSurfaces,
          },
          {
            label: 'Protected implementation',
            description: 'Risky implementation files or hotspots that those validation surfaces should cover.',
            files: validatedFiles,
          },
        ],
      });
    }

    return definitions;
  }

  function buildFlowSummaries({ repoMetadata, trackedFiles, modules, entrypoints, hotspots, subsystemLookup, activeProfile }) {
    const profileFlowDefinitions = Array.isArray(activeProfile?.flow_definitions)
      ? activeProfile.flow_definitions
      : [];
    const syntheticDefinitions = activeProfile?.id === 'generic-javascript-repo' || activeProfile?.base_profile_id === 'generic-javascript-repo'
      ? buildGenericFlowDefinitions({
          repoMetadata,
          trackedFiles,
          modules,
          entrypoints,
          hotspots,
        })
      : [];
    const definitions = [];
    const seen = new Set();
    for (const definition of [...profileFlowDefinitions, ...syntheticDefinitions]) {
      const id = typeof definition?.id === 'string' ? definition.id.trim() : '';
      if (!id || seen.has(id)) {
        continue;
      }
      seen.add(id);
      definitions.push(definition);
    }
    const availableFiles = new Set(uniquePaths(trackedFiles));

    return definitions.map((definition) => {
      const steps = definition.steps
        .map((step) => ({
          label: step.label,
          description: step.description,
          files: uniquePaths(step.files.filter(filePath => availableFiles.has(filePath))),
        }))
        .filter(step => step.files.length > 0);

      if (steps.length === 0) {
        return null;
      }

      const files = uniquePaths(steps.flatMap(step => step.files));
      return {
        id: definition.id,
        label: definition.label,
        summary: definition.summary,
        questions_it_answers: uniqueStrings(definition.questions || []),
        confidence: definition.evidence_quality === 'profile-defined' ? 'high' : 'medium',
        evidence_quality: definition.evidence_quality || (definition.steps.length >= 3 ? 'direct' : 'heuristic'),
        evidence: uniqueStrings(definition.evidence || []),
        files,
        subsystems: uniqueStrings(files.map(filePath => (subsystemLookup.get(filePath) || getSubsystemForFile(filePath, activeProfile)).label)),
        steps,
      };
    }).filter(Boolean);
  }

  function buildEntrypoints(repoMetadata, availableFiles, hotspots, modules, subsystemLookup, activeProfile, repoSignals = null) {
    const moduleMap = buildModuleEntryMap(modules);
    const reverseDeps = buildReverseDependencyMap(modules);
    const hotspotFiles = new Set((hotspots || []).map((item) => toRepoPath(item.file)).filter(Boolean));
    const rankedCandidates = uniquePaths(Array.from(availableFiles))
      .map((filePath) => {
        const candidate = inferGenericEntrypointCandidate(filePath, repoMetadata, repoSignals);
        if (!candidate) {
          return null;
        }
        const corePath = getEntrypointCorePathBoost(filePath, moduleMap, hotspotFiles, reverseDeps);
        const score = candidate.score + corePath.boost;
        return {
          ...candidate,
          score,
          confidence: score >= 155 ? 'high' : (score >= 128 ? 'medium' : 'low'),
          evidence: uniqueStrings([...(candidate.evidence || []), ...(corePath.evidence || [])]),
        };
      })
      .filter(Boolean)
      .sort((left, right) => right.score - left.score || left.file.localeCompare(right.file));

    const entrypoints = [];
    const seen = new Set();
    const entrypointLimit = (repoSignals?.frameworks || []).some((framework) => framework === '.NET' || framework === 'Python')
      || String(repoSignals?.archetype || '').includes('polyglot')
      ? 8
      : 6;

    for (const candidate of rankedCandidates) {
      if (entrypoints.length >= entrypointLimit) {
        break;
      }
      const filePath = toRepoPath(candidate.file);
      if (!filePath || seen.has(filePath) || !availableFiles.has(filePath)) {
        continue;
      }
      seen.add(filePath);
      const subsystem = subsystemLookup.get(filePath) || getSubsystemForFile(filePath, activeProfile);
      entrypoints.push({
        file: filePath,
        role: candidate.role,
        subsystem: subsystem.label,
        signal_type: isStructuredContentFile(filePath) ? 'content' : (isExecutableSurfaceFile(filePath) ? 'runtime' : 'module'),
        confidence: candidate.confidence || 'medium',
        evidence: uniqueStrings(candidate.evidence || []),
      });
    }

    const hotspotFallbacks = selectDiverseHotspots([
      ...(hotspots || []).filter(item => !isConfigOrContractFile(item.file) && SYMBOL_INDEX_EXTENSIONS.has(path.extname(item.file).toLowerCase())),
      ...(hotspots || []).filter(item => !isConfigOrContractFile(item.file)),
      ...(hotspots || []),
    ], entrypointLimit);

    for (const hotspot of hotspotFallbacks) {
      if (entrypoints.length >= entrypointLimit) {
        break;
      }
      if (seen.has(hotspot.file)) {
        continue;
      }
      seen.add(hotspot.file);
      entrypoints.push({
        file: hotspot.file,
        role: 'High-signal hotspot to orient on the repo quickly',
        subsystem: hotspot.subsystem_label,
        signal_type: hotspot.signal_type || 'module',
        confidence: hotspot.confidence === 'high' ? 'medium' : (hotspot.confidence || 'low'),
        evidence: uniqueStrings([
          'Selected from high-signal hotspot ranking.',
          hotspot.evidence?.structured_content ? 'Structured content weighting applied.' : '',
          hotspot.evidence?.executable_surface ? 'Executable surface weighting applied.' : '',
        ]),
      });
    }

    return entrypoints;
  }

