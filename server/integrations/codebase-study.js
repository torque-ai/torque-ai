  function normalizeModuleEntry(rawEntry) {
    const entry = rawEntry && typeof rawEntry === 'object' && !Array.isArray(rawEntry)
      ? rawEntry
      : {};
    const file = toRepoPath(entry.file);
    if (!file) {
      return null;
    }
    return {
      file,
      purpose: typeof entry.purpose === 'string' ? entry.purpose.trim() : '',
      exports: uniqueStrings(entry.exports),
      deps: uniqueStrings(entry.deps),
    };
  }

  function normalizeModuleIndex(rawIndex) {
    const source = rawIndex && typeof rawIndex === 'object' && !Array.isArray(rawIndex)
      ? rawIndex
      : {};
    const modules = [];
    const seen = new Set();
    for (const rawEntry of Array.isArray(source.modules) ? source.modules : []) {
      const entry = normalizeModuleEntry(rawEntry);
      if (!entry || seen.has(entry.file)) {
        continue;
      }
      seen.add(entry.file);
      modules.push(entry);
    }
    modules.sort((left, right) => left.file.localeCompare(right.file));
    return {
      modules,
      last_updated: typeof source.last_updated === 'string' && source.last_updated.trim()
        ? source.last_updated.trim()
        : null,
    };
  }

  async function readJsonIfPresent(filePath) {
    try {
      const raw = await fsPromises.readFile(filePath, 'utf8');
      return JSON.parse(raw);
    } catch {
      return null;
    }
  }

  async function readTextIfPresent(filePath) {
    try {
      return await fsPromises.readFile(filePath, 'utf8');
    } catch {
      return '';
    }
  }

  function extractReadmeIntro(content) {
    const lines = String(content || '').split(/\r?\n/);
    const collected = [];
    let started = false;
    for (const line of lines) {
      const trimmed = line.trim();
      if (!started) {
        if (!trimmed || trimmed.startsWith('#')) {
          continue;
        }
        started = true;
        collected.push(trimmed);
        continue;
      }
      if (!trimmed) {
        break;
      }
      collected.push(trimmed);
    }
    return collected.join(' ');
  }

  async function loadRepoMetadata(workingDirectory) {
    const packageJson = await readJsonIfPresent(path.join(workingDirectory, 'package.json'));
    const readmeIntro = extractReadmeIntro(await readTextIfPresent(path.join(workingDirectory, 'README.md')));
    const rootEntries = await fsPromises.readdir(workingDirectory, { withFileTypes: true }).catch(() => []);
    const solutionFiles = rootEntries
      .filter((entry) => entry && typeof entry.name === 'string' && entry.isFile && entry.isFile() && entry.name.toLowerCase().endsWith('.sln'))
      .map((entry) => toRepoPath(entry.name));
    const repoName = typeof packageJson?.name === 'string' && packageJson.name.trim()
      ? packageJson.name.trim()
      : path.basename(workingDirectory);
    const description = typeof packageJson?.description === 'string' && packageJson.description.trim()
      ? packageJson.description.trim()
      : (readmeIntro || `Architecture study for ${repoName}.`);
    const packageMain = typeof packageJson?.main === 'string' && packageJson.main.trim()
      ? toRepoPath(packageJson.main)
      : null;
    const binFiles = packageJson?.bin && typeof packageJson.bin === 'object' && !Array.isArray(packageJson.bin)
      ? uniquePaths(Object.values(packageJson.bin))
      : [];

    return {
      name: repoName,
      description,
      package_main: packageMain,
      bin_files: binFiles,
      package_json: packageJson || null,
      solution_files: solutionFiles,
      dotnet_project: solutionFiles.length > 0 || fs.existsSync(path.join(workingDirectory, 'global.json')),
      python_project: fs.existsSync(path.join(workingDirectory, 'pyproject.toml'))
        || fs.existsSync(path.join(workingDirectory, 'requirements.txt'))
        || fs.existsSync(path.join(workingDirectory, 'setup.py')),
      has_powershell_build: fs.existsSync(path.join(workingDirectory, 'scripts', 'build.ps1')),
    };
  }

  function matchesSubsystemDefinition(definition, repoPath) {
    const normalized = toRepoPath(repoPath);
    if (Array.isArray(definition.exact) && definition.exact.includes(normalized)) {
      return true;
    }
    if (Array.isArray(definition.prefixes) && definition.prefixes.some(prefix => normalized.startsWith(prefix))) {
      return true;
    }
    if (Array.isArray(definition.patterns) && definition.patterns.some(pattern => pattern.test(normalized))) {
      return true;
    }
    return false;
  }

  function buildFallbackSubsystem(repoPath) {
    const normalized = toRepoPath(repoPath);
    if (ROOT_DOC_FILES.has(normalized)) {
      return {
        id: 'docs-and-guides',
        label: 'Documentation and guides',
        description: 'Documentation, generated architecture artifacts, and reference material for humans and models.',
      };
    }
    if (!normalized.includes('/')) {
      return {
        id: 'project-root',
        label: 'Project root and shared config',
        description: 'Root config, package metadata, and entrypoints that tie the repository together.',
      };
    }
    const segments = normalized.split('/').filter(Boolean);
    const topLevel = segments[0];
    if (topLevel === 'server' && segments.length >= 2) {
      const secondLevel = segments[1];
      return {
        id: `server-${secondLevel}-area`,
        label: `server ${secondLevel}`,
        description: `Files rooted under \`server/${secondLevel}/\`.`,
      };
    }
    return {
      id: `${topLevel}-area`,
      label: `${topLevel} area`,
      description: `Files rooted under \`${topLevel}/\`.`,
    };
  }

  function getSubsystemForFile(repoPath, activeProfile) {
    const normalized = toRepoPath(repoPath);
    const definition = (activeProfile?.subsystem_definitions || []).find(def => matchesSubsystemDefinition(def, normalized));
    if (definition) {
      return {
        id: definition.id,
        label: definition.label,
        description: definition.description,
      };
    }
    return buildFallbackSubsystem(normalized);
  }

  function buildSubsystemLookup(values, activeProfile) {
    const lookup = new Map();
    for (const repoPath of uniquePaths(values)) {
      lookup.set(repoPath, getSubsystemForFile(repoPath, activeProfile));
    }
    return lookup;
  }

  function countOccurrences(values) {
    const counts = new Map();
    for (const value of values || []) {
      const normalized = String(value || '').trim();
      if (!normalized) {
        continue;
      }
      counts.set(normalized, (counts.get(normalized) || 0) + 1);
    }
    return counts;
  }

