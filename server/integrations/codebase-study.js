  function findMatchingDelimiter(source, startIndex, openChar, closeChar) {
    const normalized = String(source || '');
    let depth = 0;
    for (let index = startIndex; index < normalized.length; index += 1) {
      const char = normalized[index];
      if (char === openChar) {
        depth += 1;
      } else if (char === closeChar) {
        depth -= 1;
        if (depth === 0) {
          return index;
        }
      }
    }
    return -1;
  }

  function splitTopLevelList(value, separator = ',') {
    const normalized = String(value || '');
    if (!normalized.trim()) {
      return [];
    }
    const parts = [];
    let current = '';
    let angleDepth = 0;
    let parenDepth = 0;
    let bracketDepth = 0;
    let braceDepth = 0;

    for (const char of normalized) {
      if (char === '<') {
        angleDepth += 1;
      } else if (char === '>') {
        angleDepth = Math.max(0, angleDepth - 1);
      } else if (char === '(') {
        parenDepth += 1;
      } else if (char === ')') {
        parenDepth = Math.max(0, parenDepth - 1);
      } else if (char === '[') {
        bracketDepth += 1;
      } else if (char === ']') {
        bracketDepth = Math.max(0, bracketDepth - 1);
      } else if (char === '{') {
        braceDepth += 1;
      } else if (char === '}') {
        braceDepth = Math.max(0, braceDepth - 1);
      }

      if (
        char === separator
        && angleDepth === 0
        && parenDepth === 0
        && bracketDepth === 0
        && braceDepth === 0
      ) {
        if (current.trim()) {
          parts.push(current.trim());
        }
        current = '';
        continue;
      }

      current += char;
    }

    if (current.trim()) {
      parts.push(current.trim());
    }
    return parts;
  }

  function collectCSharpSignatureBlock(lines, startIndex, maxLines = 6) {
    const fragments = [];
    let parenDepth = 0;
    for (let index = startIndex; index < lines.length && fragments.length < maxLines; index += 1) {
      const trimmed = String(lines[index] || '').trim();
      if (!trimmed || trimmed.startsWith('//')) {
        if (fragments.length > 0 && parenDepth === 0) {
          break;
        }
        continue;
      }
      fragments.push(trimmed);
      for (const char of trimmed) {
        if (char === '(') {
          parenDepth += 1;
        } else if (char === ')') {
          parenDepth = Math.max(0, parenDepth - 1);
        }
      }
      if ((trimmed.includes('{') || trimmed.endsWith(';')) && parenDepth === 0) {
        break;
      }
      if (parenDepth === 0 && trimmed.includes(')')) {
        break;
      }
    }
    return fragments.join(' ');
  }

