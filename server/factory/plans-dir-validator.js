'use strict';

const path = require('path');

function normalizeForCompare(value) {
  const resolved = path.resolve(value);
  return process.platform === 'win32' ? resolved.toLowerCase() : resolved;
}

function isSameOrInside(candidate, parent) {
  const normalizedCandidate = normalizeForCompare(candidate);
  const normalizedParent = normalizeForCompare(parent);
  if (normalizedCandidate === normalizedParent) {
    return true;
  }
  const relative = path.relative(normalizedParent, normalizedCandidate);
  return Boolean(relative)
    && !relative.startsWith('..')
    && !path.isAbsolute(relative);
}

function validatePlansDir({ projectPath, plansDir }) {
  if (typeof projectPath !== 'string' || !projectPath.trim()) {
    throw new Error('projectPath is required');
  }
  if (typeof plansDir !== 'string' || !plansDir.trim()) {
    throw new Error('plans_dir is required');
  }

  const resolvedProjectPath = path.resolve(projectPath);
  const resolvedPlansDir = path.resolve(plansDir);
  if (!isSameOrInside(resolvedPlansDir, resolvedProjectPath)) {
    throw new Error(`plans_dir must stay inside project path: ${resolvedPlansDir}`);
  }

  const autoGeneratedPlansDir = path.join(
    resolvedProjectPath,
    'docs',
    'superpowers',
    'plans',
    'auto-generated',
  );
  if (isSameOrInside(resolvedPlansDir, autoGeneratedPlansDir)) {
    throw new Error('plans_dir must not point at docs/superpowers/plans/auto-generated');
  }

  return resolvedPlansDir;
}

