const r = require('../api/routes');
const d = require('../api/v2-dispatch');
const rn = new Set(r.filter(x => x.handlerName && x.handlerName.startsWith('handleV2Cp')).map(x => x.handlerName));
const dn = new Set(d.v2CpRoutes.map(x => x.handlerName));
const missing = [...rn].filter(n => !dn.has(n));
const extra = [...dn].filter(n => !rn.has(n));
console.log('routes unique:', rn.size, 'dispatch unique:', dn.size);
if (missing.length) console.log('Missing from dispatch:', JSON.stringify(missing));
if (extra.length) console.log('Extra in dispatch:', JSON.stringify(extra));
if (!missing.length && !extra.length) console.log('PARITY MATCH');
