'use strict';

const {
  buildAutoGeneratedPlanPrompt,
  resolvePlanGenerationTimeoutMinutes,
  buildPlanGenerationActivityTimeoutPolicy,
  getEffectiveProjectProvider,
  OLLAMA_PLAN_GENERATION_TIMEOUT_MINUTES,
  DEFAULT_PLAN_GENERATION_TIMEOUT_MINUTES,
} = require('../factory/loop-controller');
const {
  buildStarvationRecoveryScope,
} = require('../factory/starvation-recovery');

const ollamaProject = {
  id: 'p-ollama',
  name: 'DLPhone',
  path: '/proj',
  brief: 'Mobile RTS.',
  config_json: JSON.stringify({
    provider_lane_policy: {
      expected_provider: 'ollama',
      allowed_providers: ['ollama'],
      enforce_handoffs: true,
    },
  }),
};

const codexProject = {
  id: 'p-codex',
  name: 'OtherProj',
  path: '/proj2',
  brief: 'A node project.',
  config_json: JSON.stringify({
    provider_lane_policy: {
      expected_provider: 'codex',
      allowed_providers: ['codex'],
      enforce_handoffs: true,
    },
  }),
};

const projectNoLane = {
  id: 'p-default',
  name: 'NoLane',
  path: '/proj3',
  brief: 'No lane policy.',
};

const workItem = {
  id: 99,
  title: 'Trim test',
  description: 'A test work item description.',
};

describe('Phase G: ollama-friendly architect prompt', () => {
  let prevEnv;
  beforeEach(() => {
    prevEnv = process.env.TORQUE_CODEGRAPH_ENABLED;
  });
  afterEach(() => {
    if (prevEnv === undefined) delete process.env.TORQUE_CODEGRAPH_ENABLED;
    else process.env.TORQUE_CODEGRAPH_ENABLED = prevEnv;
  });

  it('skips the codegraph research section when project provider is ollama', () => {
    process.env.TORQUE_CODEGRAPH_ENABLED = '1';
    const out = buildAutoGeneratedPlanPrompt(ollamaProject, workItem);
    expect(out).not.toContain('Code-graph research');
    expect(out).not.toContain('cg_class_hierarchy');
    expect(out).not.toContain('cg_impact_set');
  });

  it('still includes the codegraph section for non-ollama projects (codex)', () => {
    process.env.TORQUE_CODEGRAPH_ENABLED = '1';
    const out = buildAutoGeneratedPlanPrompt(codexProject, workItem);
    expect(out).toContain('Code-graph research');
    expect(out).toContain('cg_class_hierarchy');
  });

  it('still includes codegraph for projects without a provider lane policy', () => {
    process.env.TORQUE_CODEGRAPH_ENABLED = '1';
    const out = buildAutoGeneratedPlanPrompt(projectNoLane, workItem);
    expect(out).toContain('Code-graph research');
  });

  it('honors TORQUE_CODEGRAPH_ENABLED=0 even for non-ollama projects', () => {
    process.env.TORQUE_CODEGRAPH_ENABLED = '0';
    const out = buildAutoGeneratedPlanPrompt(codexProject, workItem);
    expect(out).not.toContain('Code-graph research');
  });

  it('ollama prompt is meaningfully shorter than codex prompt', () => {
    process.env.TORQUE_CODEGRAPH_ENABLED = '1';
    const ollamaOut = buildAutoGeneratedPlanPrompt(ollamaProject, workItem);
    const codexOut = buildAutoGeneratedPlanPrompt(codexProject, workItem);
    // Codegraph section is ~1600 chars; ollama prompt should be at
    // least ~1000 chars shorter than the codex one.
    expect(codexOut.length - ollamaOut.length).toBeGreaterThan(1000);
  });

  it('ollama prompt still includes project context, scope rules, and description', () => {
    const out = buildAutoGeneratedPlanPrompt(ollamaProject, {
      ...workItem,
      origin: { exemplar_files: ['simtests/Foo.cs'] },
    });
    expect(out).toContain('Project context:');
    expect(out).toContain('Files in scope');
    expect(out).toContain('`simtests/Foo.cs`');
    expect(out).toContain('A test work item description.');
    expect(out).toContain('Use `## Task N:` headings');
  });

  it('reads provider from a parsed `config` object as well as `config_json`', () => {
    process.env.TORQUE_CODEGRAPH_ENABLED = '1';
    const projectWithConfigObj = {
      id: 'p',
      name: 'X',
      path: '/',
      brief: 'b',
      config: {
        provider_lane_policy: { expected_provider: 'OLLAMA', enforce_handoffs: true },
      },
    };
    const out = buildAutoGeneratedPlanPrompt(projectWithConfigObj, workItem);
    expect(out).not.toContain('Code-graph research');
  });

  it('survives a malformed config_json string without throwing', () => {
    process.env.TORQUE_CODEGRAPH_ENABLED = '1';
    const broken = { id: 'p', name: 'X', path: '/', brief: 'b', config_json: '{not json' };
    expect(() => buildAutoGeneratedPlanPrompt(broken, workItem)).not.toThrow();
    const out = buildAutoGeneratedPlanPrompt(broken, workItem);
    // Falls back to non-ollama default (codegraph included).
    expect(out).toContain('Code-graph research');
  });
});

describe('Phase G: getEffectiveProjectProvider', () => {
  it('returns lowercase expected_provider from config_json', () => {
    expect(getEffectiveProjectProvider(ollamaProject)).toBe('ollama');
    expect(getEffectiveProjectProvider(codexProject)).toBe('codex');
  });

  it('returns lowercase expected_provider from parsed config object', () => {
    const p = { config: { provider_lane_policy: { expected_provider: 'OLLAMA' } } };
    expect(getEffectiveProjectProvider(p)).toBe('ollama');
  });

  it('returns null when no lane policy is set', () => {
    expect(getEffectiveProjectProvider(projectNoLane)).toBe(null);
    expect(getEffectiveProjectProvider({})).toBe(null);
    expect(getEffectiveProjectProvider(null)).toBe(null);
  });

  it('returns null on malformed config_json', () => {
    expect(getEffectiveProjectProvider({ config_json: '{not json' })).toBe(null);
  });

  it('returns null when expected_provider is empty/whitespace', () => {
    const p = { config: { provider_lane_policy: { expected_provider: '   ' } } };
    expect(getEffectiveProjectProvider(p)).toBe(null);
  });
});

describe('Phase G: resolvePlanGenerationTimeoutMinutes', () => {
  it('returns the ollama-specific cap (10 min) for ollama-pinned projects', () => {
    expect(resolvePlanGenerationTimeoutMinutes(ollamaProject))
      .toBe(OLLAMA_PLAN_GENERATION_TIMEOUT_MINUTES);
    expect(OLLAMA_PLAN_GENERATION_TIMEOUT_MINUTES).toBe(10);
  });

  it('returns the standard default (30 min) for codex-pinned projects', () => {
    expect(resolvePlanGenerationTimeoutMinutes(codexProject))
      .toBe(DEFAULT_PLAN_GENERATION_TIMEOUT_MINUTES);
  });

  it('returns the standard default for projects without a lane policy', () => {
    expect(resolvePlanGenerationTimeoutMinutes(projectNoLane))
      .toBe(DEFAULT_PLAN_GENERATION_TIMEOUT_MINUTES);
  });

  it('honors an explicit project config override even for ollama', () => {
    const p = {
      ...ollamaProject,
      config_json: JSON.stringify({
        provider_lane_policy: ollamaProject.config_json
          ? JSON.parse(ollamaProject.config_json).provider_lane_policy
          : null,
        plan_generation_timeout_minutes: 25,
      }),
    };
    expect(resolvePlanGenerationTimeoutMinutes(p)).toBe(25);
  });

  it('builds bounded runtime policy evidence from the resolved timeout', () => {
    expect(buildPlanGenerationActivityTimeoutPolicy(30)).toEqual({
      kind: 'plan_generation',
      timeout_minutes: 30,
      max_wall_clock_minutes: 60,
      overrun_intake_problem: 'timeout_overrun_active',
    });
    expect(buildPlanGenerationActivityTimeoutPolicy(25)).toEqual(expect.objectContaining({
      timeout_minutes: 25,
      max_wall_clock_minutes: 50,
    }));
    expect(buildPlanGenerationActivityTimeoutPolicy(75)).toEqual(expect.objectContaining({
      timeout_minutes: 75,
      max_wall_clock_minutes: 120,
    }));
  });
});

describe('Phase G: buildStarvationRecoveryScope — concrete-item bias', () => {
  it('explicitly prefers concrete_factory_work_items over loose patterns', () => {
    // fc73c9c9 ("bound scouts and extend active timeouts") reworded the
    // scope to drop the loose-patterns escape hatch entirely — the section
    // is now headed "Output format — concrete work items only" and the
    // "strongly prefer" / "concrete is preferred" phrasings were replaced
    // with direct prescriptive language.
    const scope = buildStarvationRecoveryScope({
      project: { name: 'DLPhone', brief: 'Mobile RTS.' },
      noYieldScoutCount: 0,
    });
    expect(scope).toContain('Output format');
    expect(scope).toContain('concrete_factory_work_items');
    expect(scope).toMatch(/concrete work items only/i);
    expect(scope).toMatch(/much higher rates than loose patterns/i);
  });

  it('explains the concrete item shape (allowed_files + verification)', () => {
    const scope = buildStarvationRecoveryScope({
      project: { name: 'X', brief: 'b' },
      noYieldScoutCount: 0,
    });
    expect(scope).toContain('`allowed_files`');
    expect(scope).toContain('`verification`');
    expect(scope).toContain('`title`');
    expect(scope).toContain('`why`');
  });

  it('keeps the existing evidence-requirement and scope-bounds sections', () => {
    const scope = buildStarvationRecoveryScope({
      project: { name: 'DLPhone', brief: 'Mobile RTS.' },
      noYieldScoutCount: 2,
    });
    expect(scope).toContain('## Evidence requirement');
    expect(scope).toContain('## Scope bounds');
    expect(scope).toContain('No-yield scout backoff count: 2');
  });

  it('allows empty patterns AND empty concrete_factory_work_items as a valid signal', () => {
    const scope = buildStarvationRecoveryScope({
      project: { name: 'DLPhone', brief: 'Mobile RTS.' },
      noYieldScoutCount: 0,
    });
    expect(scope).toMatch(/empty `concrete_factory_work_items` array/i);
    expect(scope).toMatch(/empty result is a valid signal/i);
  });
});
