describe('auto-generated plan description quality scoring', () => {
  it('passes a high-quality plan with file path, scope, success criteria, and validation command', () => {
    const loopController = require('../factory/loop-controller');

    const score = loopController._internalForTests.scoreAutoGeneratedPlanDescriptions(HIGH_QUALITY_PLAN);

    expect(score.blocked).toBe(false);
    expect(score.tasks[0].score).toBeGreaterThanOrEqual(score.threshold);
    expect(score.tasks[0].missing_signals).toEqual([]);
  });

  it('fails a structurally valid vague plan with no file paths', () => {
    const loopController = require('../factory/loop-controller');

    const score = loopController._internalForTests.scoreAutoGeneratedPlanDescriptions(VAGUE_PLAN_WITH_NO_FILE_PATHS);

    expect(score.blocked).toBe(true);
    expect(score.failures[0]).toMatchObject({
      task_index: 1,
      threshold: 80,
    });
    expect(score.failures[0].missing_signals).toContain('explicit_file_paths');
    expect(score.failures[0].missing_signals).toContain('estimated_scope');
    expect(score.failures[0].missing_signals).toContain('concrete_language');
  });

  it('passes a documentation-only plan without synthetic line counts or pytest invocations', () => {
    // Guards the bitsy 2026-04-20 item 490 / 491 regression: the deterministic
    // gate kept rejecting doc-only tasks at 60/100 because `estimated_scope`
    // and `validation_steps` regex target test-run commands ("pytest", "npx
    // vitest", etc.). Doc-removal / doc-supersession tasks naturally lack
    // these signals. When every extracted file path is a documentation
    // extension, auto-satisfy both signals so well-specified doc work gets
    // through without the planner fabricating fake pytest commands.
    const loopController = require('../factory/loop-controller');

    const DOC_ONLY_PLAN = [
      '# Doc Plan',
      '',
      '**Source:** auto-generated from work_item #1',
      '**Tech Stack:** Python',
      '',
      '## Task 1: Mark PixelLab-era design documents as superseded',
      '',
      '- [ ] **Step 1: Add supersession banners**',
      '',
      '    Edit docs/plans/2026-02-24-pixellab-integration-design.md and docs/plans/2026-03-07-style-consistency-design.md. Add top-of-file notices stating the docs are historical and superseded. Acceptance criteria: both docs must show the supersession notice before original content, and must not imply the active package wraps the PixelLab API.',
      '',
    ].join('\n');

    const score = loopController._internalForTests.scoreAutoGeneratedPlanDescriptions(DOC_ONLY_PLAN);

    expect(score.blocked).toBe(false);
    expect(score.tasks[0].score).toBeGreaterThanOrEqual(score.threshold);
    // The carve-out flips these two regex signals on for doc-only paths.
    expect(score.tasks[0].signals.estimated_scope).toBe(true);
    expect(score.tasks[0].signals.validation_steps).toBe(true);
  });

  it('still rejects a doc-only plan that lacks success criteria or concrete language', () => {
    // The carve-out only relaxes scope+validation for doc-only paths; the
    // other three signals (explicit_file_paths, success_criteria,
    // concrete_language) still have to pass on their own so vague
    // "update docs" one-liners are still rejected. The vague phrase is
    // placed far enough from the file path that the 200-char
    // hasObjectLevelDetail window doesn't rescue it.
    const loopController = require('../factory/loop-controller');

    const VAGUE_DOC_PLAN = [
      '# Vague Doc Plan',
      '',
      '**Source:** auto-generated from work_item #1',
      '**Tech Stack:** Python',
      '',
      '## Task 1: Update the PixelLab-era plan docs to tidy them up',
      '',
      '    Improve and clean up the PixelLab-era plan documents so readers have a gentler onboarding experience across the supersession notice, the historical framing, and the residual narrative framing that is no longer relevant to active work in the current package layout that ships today.',
      '',
      '    Touches docs/plans/historical/legacy.md only.',
      '',
    ].join('\n');

    const score = loopController._internalForTests.scoreAutoGeneratedPlanDescriptions(VAGUE_DOC_PLAN);

    expect(score.blocked).toBe(true);
    expect(score.failures[0].missing_signals).toContain('success_criteria');
    expect(score.failures[0].missing_signals).toContain('concrete_language');
  });

  it('augments rejected task specificity from work-item origin scope and verification', () => {
    const loopController = require('../factory/loop-controller');

    const underSpecifiedPlan = [
      '# Origin-Backed Plan',
      '',
      '**Source:** auto-generated from work_item #2048',
      '**Tech Stack:** Python',
      '',
      '## Task 1: Reconcile first-run smoke validation',
      '',
      '    Reconcile the first-run host join smoke validation for the factory work item.',
      '',
    ].join('\n');
    const workItem = {
      id: 2048,
      origin_json: JSON.stringify({
        allowed_files: [
          'tools/validate_first_run_smoke.py',
          'docs/qa/UnityFirstRunHostJoinSmokeChecklist.md',
        ],
        verification: 'python tools/validate_first_run_smoke.py docs/qa/UnityFirstRunHostJoinSmokeChecklist.md',
      }),
    };

    const augmented = loopController._internalForTests.augmentAutoGeneratedTaskSpecificity(underSpecifiedPlan, workItem);
    const score = loopController._internalForTests.scoreAutoGeneratedPlanDescriptions(augmented);

    expect(augmented).toContain('Estimated scope: single focused change across 2 files');
    expect(augmented).toContain('Validation: Run `python tools/validate_first_run_smoke.py docs/qa/UnityFirstRunHostJoinSmokeChecklist.md`');
    expect(score.blocked).toBe(false);
    expect(score.tasks[0].missing_signals).toEqual([]);
  });
});

