'use strict';
/* global describe, it, expect, beforeEach, afterEach */

const { buildAutoGeneratedPlanPrompt } = require('../factory/loop-controller');

describe('buildAutoGeneratedPlanPrompt — codegraph integration', () => {
  let prevEnv;

  beforeEach(() => {
    prevEnv = process.env.TORQUE_CODEGRAPH_ENABLED;
  });
  afterEach(() => {
    if (prevEnv === undefined) delete process.env.TORQUE_CODEGRAPH_ENABLED;
    else process.env.TORQUE_CODEGRAPH_ENABLED = prevEnv;
  });

  const project = { id: 1, name: 'demo', path: '/tmp/x', brief: 'A demo project.' };
  const workItem = { id: 7, title: 'Refactor BaseProvider', description: 'Change the BaseProvider abstract class to add a new lifecycle hook.' };

  it('includes the codegraph research section when TORQUE_CODEGRAPH_ENABLED=1', () => {
    process.env.TORQUE_CODEGRAPH_ENABLED = '1';
    const out = buildAutoGeneratedPlanPrompt(project, workItem);
    expect(out).toContain('Code-graph research');
    expect(out).toContain('cg_class_hierarchy');
    expect(out).toContain('cg_impact_set');
    expect(out).toContain('cg_find_references');
  });

  it('omits the codegraph section when the env var is unset', () => {
    delete process.env.TORQUE_CODEGRAPH_ENABLED;
    const out = buildAutoGeneratedPlanPrompt(project, workItem);
    expect(out).not.toContain('Code-graph research');
    expect(out).not.toContain('cg_class_hierarchy');
  });

  it('omits the codegraph section when the env var is "0"', () => {
    process.env.TORQUE_CODEGRAPH_ENABLED = '0';
    const out = buildAutoGeneratedPlanPrompt(project, workItem);
    expect(out).not.toContain('Code-graph research');
  });

  it('codegraph section sits between specificity signals and project context', () => {
    process.env.TORQUE_CODEGRAPH_ENABLED = '1';
    const out = buildAutoGeneratedPlanPrompt(project, workItem);
    const cgIdx = out.indexOf('Code-graph research');
    const specIdx = out.indexOf('Concrete language');
    const projIdx = out.indexOf('Project context:');
    // specificity signals → codegraph section → project context
    expect(specIdx).toBeGreaterThan(0);
    expect(cgIdx).toBeGreaterThan(specIdx);
    expect(projIdx).toBeGreaterThan(cgIdx);
  });

  it('preserves the priorFeedback prefix when codegraph is enabled', () => {
    process.env.TORQUE_CODEGRAPH_ENABLED = '1';
    const out = buildAutoGeneratedPlanPrompt(project, workItem, 'Prior reviewer said: be more specific.');
    expect(out.startsWith('Prior reviewer said')).toBe(true);
    expect(out).toContain('Code-graph research');
  });
});
