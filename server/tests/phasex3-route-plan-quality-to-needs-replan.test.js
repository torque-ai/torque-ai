'use strict';

const fs = require('fs');
const path = require('path');

describe('Phase X3: plan-quality failures route to needs_replan, not rejected', () => {
  const src = fs.readFileSync(
    path.join(__dirname, '..', 'factory', 'loop-controller.js'),
    'utf8',
  );

  describe('removed terminal-rejection paths', () => {
    it('PLAN_QUALITY_REJECT_CAP constant is gone (replaced by soft threshold)', () => {
      // The old cap caused work items to move to status:rejected after N
      // attempts. Phase X3 removes that — only the soft threshold remains
      // for observability.
      expect(src).not.toMatch(/PLAN_QUALITY_REJECT_CAP\s*=/);
    });

    it('auto_rejected_plan_quality_exhausted decision action is gone', () => {
      // This was the decision-log action emitted when the cap fired and
      // moved the item to rejected. Phase X3 replaced it with the
      // soft-threshold-crossed observability action.
      expect(src).not.toContain('auto_rejected_plan_quality_exhausted');
    });

    it('plan_quality_exhausted_after_N_attempts reject_reason format is gone', () => {
      expect(src).not.toContain('plan_quality_exhausted_after_');
    });

    it('returnAutoGeneratedPlanToPrioritizeForDescriptionQuality writes status:needs_replan, not status:rejected or status:prioritized', () => {
      // Find the function body and verify the updateWorkItem call uses
      // needs_replan. Match the function signature, then look for
      // updateWorkItem within ~3000 chars after.
      const fnIdx = src.indexOf('function returnAutoGeneratedPlanToPrioritizeForDescriptionQuality');
      expect(fnIdx).toBeGreaterThan(-1);
      const body = src.slice(fnIdx, fnIdx + 4000);
      // The single remaining updateWorkItem inside this function must
      // route to needs_replan.
      expect(body).toMatch(/updateWorkItem\(workItem\.id,\s*\{[\s\S]{0,200}status:\s*'needs_replan'/);
      // And no longer to 'rejected' from this code path.
      expect(body).not.toMatch(/updateWorkItem\(workItem\.id,\s*\{[\s\S]{0,200}status:\s*'rejected'/);
      // And no longer to 'prioritized' (which was the OLD intra-cap path).
      expect(body).not.toMatch(/updateWorkItem\(workItem\.id,\s*\{[\s\S]{0,200}status:\s*'prioritized'/);
    });
  });

  describe('soft-threshold observability still in place', () => {
    it('PLAN_QUALITY_SOFT_THRESHOLD constant exists', () => {
      const match = src.match(/PLAN_QUALITY_SOFT_THRESHOLD\s*=\s*(\d+)/);
      expect(match).not.toBeNull();
      expect(Number(match[1])).toBeGreaterThanOrEqual(3);
    });

    it('plan_quality_soft_threshold_crossed decision is logged when crossed', () => {
      expect(src).toContain('plan_quality_soft_threshold_crossed');
    });

    it('soft-threshold log message states the work item is NOT being rejected', () => {
      // Critical: the log must make it explicit that we're continuing,
      // not abandoning. This is the contract with the operator.
      expect(src).toMatch(/soft.*threshold[\s\S]{0,400}NOT being rejected/);
    });
  });

  describe('decision log + return value reflect needs_replan routing', () => {
    it('replaces the legacy returned_to_prioritize action with routed_to_needs_replan', () => {
      expect(src).toContain('plan_description_quality_routed_to_needs_replan');
    });

    it('stage_result.status is needs_replan (was returned_to_prioritize)', () => {
      const fnIdx = src.indexOf('function returnAutoGeneratedPlanToPrioritizeForDescriptionQuality');
      const body = src.slice(fnIdx, fnIdx + 5000);
      expect(body).toMatch(/stage_result:\s*\{[\s\S]{0,200}status:\s*'needs_replan'/);
    });
  });
});
