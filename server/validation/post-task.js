/**
 * Parse a command string into executable and args, respecting quoted segments.
 * Handles both single and double quotes. Falls back to simple split for unquoted strings.
 * @param {string} cmdString - e.g. 'npm run build' or 'npx tsc --project "path with spaces"'
 * @returns {{ executable: string, args: string[] }}
 */
function parseCommand(cmdString) {
  const tokens = [];
  let current = '';
  let inSingle = false;
  let inDouble = false;

  for (let i = 0; i < cmdString.length; i++) {
    const ch = cmdString[i];
    if (ch === "'" && !inDouble) {
      inSingle = !inSingle;
    } else if (ch === '"' && !inSingle) {
      inDouble = !inDouble;
    } else if (ch === ' ' && !inSingle && !inDouble) {
      if (current.length > 0) {
        tokens.push(current);
        current = '';
      }
    } else {
      current += ch;
    }
  }
  if (current.length > 0) tokens.push(current);

  return {
    executable: tokens[0] || '',
    args: tokens.slice(1)
  };
}

